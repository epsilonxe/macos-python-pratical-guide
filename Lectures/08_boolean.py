x = 16
y = 7


# Check: x > y
is_x_greater_than_y = x > y
print(is_x_greater_than_y)

# Comparison

# Equality
print(x == y)

# Inequality
print(x > y)
print(x >= y)
print(x < y)
print(x <= y)
print(x != y)

# Boolean Operations
condition_1 = x > y
condition_2 = x < y

print(condition_1)

print(condition_1)



new_condition = condition_1 and condition_2
print(new_condition)


new_condition = condition_1 or condition_2
print(new_condition)

